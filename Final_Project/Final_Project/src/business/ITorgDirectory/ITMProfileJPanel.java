private void fetchUserInfo() {
        // Database connection parameters

        try {
            Connection connection = DatabaseConnection.getConnection();

            String query = "SELECT * FROM itsupport WHERE Role = 'IT Manager'";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {

                try (ResultSet rs = preparedStatement.executeQuery()) {
                    if (rs.next()) {
                        // Populate the UI components with user information
                        txtName.setText(rs.getString("Name"));
                        txtRole.setText(rs.getString("Role"));
                        txtContact.setText(rs.getString("Contact"));
                        txtEmail.setText(rs.getString("Email"));
                        txtDepartment.setText(rs.getString("Department"));
                    } else {
                        // Handle the case where no user information is found.
                    }

                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            // Handle the exception, for example, show an error dialog.
            JOptionPane.showMessageDialog(this, "Error fetching user information: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

