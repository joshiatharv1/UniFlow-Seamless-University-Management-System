    private void loadFacultyData(String username) {
        String query = "SELECT * FROM faculty WHERE Username = ?";
        try (Connection connection = DatabaseConnection.getConnection();
                PreparedStatement pst = connection.prepareStatement(query)) {
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                // Populate the text fields with data from the database
                txtname.setText(rs.getString("Name"));
                txtEmployeeID.setText(String.valueOf(rs.getInt("EmployeeID")));
                txtcontact.setText(rs.getString("Contact"));
                txtdob.setText(rs.getString("DOB"));
                txtgender.setText(rs.getString("Gender"));
                txtnation.setText(rs.getString("Nationality"));
                txtDesignation.setText(rs.getString("Position"));
                txtenrollstat.setText(rs.getString("Email"));
                txtcredit.setText(rs.getString("Department"));
                txtemergency.setText(rs.getString("EmergencyContact"));
            } else {
                JOptionPane.showMessageDialog(this, "Faculty member not found in the database.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error fetching faculty data: " + ex.getMessage());
        }
    }

