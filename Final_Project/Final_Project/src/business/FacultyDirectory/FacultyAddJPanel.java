    private void btnaddcourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddcourseActionPerformed
        // TODO add your handling code here:
        String courseID = txtCourseID.getText();
    String courseName = txtCourseName.getText();
    String description = txtCourseDescription.getText();
    int credits = Integer.parseInt(jTextField5.getText());
    int maxStudents = Integer.parseInt(jTextField6.getText());
    String schedule = jTextField7.getText();
    String employeeID = getEmployeeIDFromUsername(username);

    // Check if the employee already has 2 courses
    if (getNumberOfCoursesForEmployee(employeeID) >= 2) {
        JOptionPane.showMessageDialog(this, "Error: The employee already has 2 courses.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Check if the CourseID already exists
    if (isCourseIDExists(courseID)) {
        JOptionPane.showMessageDialog(this, "Error: Course with the same ID already exists.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Perform the insertion of the new course into the database
    try {
        String insertQuery = "INSERT INTO courses (CourseID, CourseName, Description, Credits, EmployeeID, MaximumStudents, Schedule) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement preparedStatement = connection.prepareStatement(insertQuery);
        preparedStatement.setString(1, courseID);
        preparedStatement.setString(2, courseName);
        preparedStatement.setString(3, description);
        preparedStatement.setInt(4, credits);
        preparedStatement.setString(5, employeeID);
        preparedStatement.setInt(6, maxStudents);
        preparedStatement.setString(7, schedule);

        int rowsAffected = preparedStatement.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Course added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error: Failed to add the course.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: Failed to add the course. Please check the console for details.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Reset text fields after successful addition
    txtCourseID.setText("");
    txtCourseName.setText("");
    txtCourseDescription.setText("");
    jTextField5.setText("");
    jTextField6.setText("");
    jTextField7.setText("");
    }//GEN-LAST:event_btnaddcourseActionPerformed

