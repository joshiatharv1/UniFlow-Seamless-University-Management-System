     private boolean isCourseIDExists(String courseID) {
        try {
            String query = "SELECT * FROM courses WHERE CourseID = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, courseID);
            ResultSet resultSet = preparedStatement.executeQuery();
            return resultSet.next();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    }

    private int getNumberOfCoursesForEmployee(String employeeID) {
        try {
            String query = "SELECT COUNT(*) FROM courses WHERE EmployeeID = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, employeeID);
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                return resultSet.getInt(1);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return 0;
    }

    private String getEmployeeIDFromUsername(String username) {
        try {
            String query = "SELECT EmployeeID FROM faculty WHERE Username = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, username);
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                return resultSet.getString("EmployeeID");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return null;
    }


