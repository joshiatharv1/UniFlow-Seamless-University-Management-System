  private void fetchUserInfo() {
        // Database connection parameters

        try {
            Connection connection = DatabaseConnection.getConnection();

            String query = "SELECT * FROM Librarians  WHERE Position = 'Cataloging Specialist'";
            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {

                try (ResultSet rs = preparedStatement.executeQuery()) {
                    if (rs.next()) {
                        // Populate the UI components with user information
                        txtname.setText(rs.getString("Name"));
                        txtposition.setText(rs.getString("Position"));
                        txtPhone.setText(rs.getString("Contact"));
                        txtEmail.setText(rs.getString("Email"));
                        txtContract.setText(rs.getString("ContractType"));
                    } else {
                    }
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }


