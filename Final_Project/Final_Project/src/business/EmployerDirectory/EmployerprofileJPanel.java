    private void fetchUserInfo(String username)
    {
    PreparedStatement preparedStatement = null;
    ResultSet resultSet = null;
    try {
        // Establish database connection
        Connection connection = DatabaseConnection.getConnection();

        // SQL query to fetch employer details based on username
        String sqlQuery = "SELECT * FROM employers WHERE Username = ?";
        preparedStatement = connection.prepareStatement(sqlQuery);
        preparedStatement.setString(1, username);
        
        // Execute the query and get the result set
        resultSet = preparedStatement.executeQuery();

        // Check if the result set has data
        if (resultSet.next()) {
            // Set retrieved data to relevant text fields
            txtname.setText(resultSet.getString("CompanyName"));
            txtemployerID.setText(String.valueOf(resultSet.getInt("EmployerID")));
            txtCompanyName.setText(resultSet.getString("CompanyName"));
            txtIndustryName.setText(resultSet.getString("IndustryName"));
            txtCompanyDescription.setText(resultSet.getString("CompanyDescription"));
            txtExperience.setText(String.valueOf(resultSet.getInt("Experience")));
            txtemail.setText(resultSet.getString("ContactPersonEmail"));
            txtlocation.setText(resultSet.getString("Location"));
            txtWebsite.setText(resultSet.getString("Website"));
            txtDesignation.setText(resultSet.getString("Designation"));
        } else {
            // Handle the case where no data is found for the given username
            // You can show an error message or perform any other necessary action
        }
    } catch (SQLException e) {
        // Handle any SQL exceptions that may occur
        e.printStackTrace();
    } finally {
        // Close the database resources to prevent memory leaks
        try {
             Connection connection = DatabaseConnection.getConnection();
            if (resultSet != null) resultSet.close();
            if (preparedStatement != null) preparedStatement.close();
            if (connection != null) connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
  
    }
