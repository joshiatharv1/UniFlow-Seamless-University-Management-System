    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DatabaseConnection.getConnection();
            String username = txtusername.getText();
            char[] passwordChars = txtpassword.getPassword();
            String password = new String(passwordChars);
            String query = "SELECT * FROM employers WHERE Username=? AND Password=?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, username);
            preparedStatement.setString(2, password);

            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                JOptionPane.showMessageDialog(null, "Welcome Employer!");
                EmployerJPanel employerPanel = new EmployerJPanel(username);
                employerPanel.setUsername(username); // Set the username dynamically

                // Set up the StudentJPanel in a JFrame
                JFrame employerFrame = new JFrame("");
                employerFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                employerFrame.setSize(900, 800);
                employerFrame.setLocationRelativeTo(null);
                employerFrame.add(employerPanel);
                employerFrame.setVisible(true);

                // Close the current login frame
                dispose();
            } else {
                // Check faculty and employer tables similarly...
                // Display appropriate messages for faculty and employer logins
                JOptionPane.showMessageDialog(null, "Invalid login credentials!");
            }

            resultSet.close();
            preparedStatement.close();
            connection.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed
