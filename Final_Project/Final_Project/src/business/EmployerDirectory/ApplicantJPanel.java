    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String jobIDInput = txtJobID.getText();

    // Establish database connection
    try (Connection connection = DatabaseConnection.getConnection()) {
        // Prepare SQL query to retrieve data based on JobID
        String sql = "SELECT ApplicationID, StudentID, Applied FROM job_applications WHERE JobID = ?";
        try (PreparedStatement statement = connection.prepareStatement(sql)) {
            // Set the JobID parameter in the SQL query
            statement.setString(1, jobIDInput);

            // Execute the query and get the result set
            try (ResultSet resultSet = statement.executeQuery()) {
                // Create a DefaultTableModel to hold the data
                DefaultTableModel model = new DefaultTableModel();
                // Add columns to the table model
                model.addColumn("ApplicationID");
                model.addColumn("StudentID");
                model.addColumn("Applied");

                // Populate the table model with retrieved data
                while (resultSet.next()) {
                    int applicationID = resultSet.getInt("ApplicationID");
                    int studentID = resultSet.getInt("StudentID");
                    boolean applied = resultSet.getBoolean("Applied");

                    // Add a row to the table model
                    model.addRow(new Object[]{applicationID, studentID, applied});
                }

                // Set the table model to the tblApplicants JTable
                tblApplicants.setModel(model);
            }
        }
    } catch (SQLException e) {
        // Handle any SQL exceptions (display error message or log the exception)
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSearchActionPerformed


