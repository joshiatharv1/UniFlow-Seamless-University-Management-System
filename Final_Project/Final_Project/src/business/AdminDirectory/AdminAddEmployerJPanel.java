    private void btnAdminAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminAddActionPerformed
        // TODO add your handling code here:
         // Get values from text fields
         String EmployerID = txtEmployerID.getText();
        String companyName = txtCompanyName.getText();
        String industryName = txtIndustryName.getText();
        String companyDescription = txtCompanyDescription.getText();
        String location = txtLocation.getText();
        String website = txtWebsite.getText();
        String contactPersonName = txtName.getText();
        String contactPersonPhone = txtPhone.getText();
        String contactPersonEmail = txtEmail.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        

        try (Connection connection = DatabaseConnection.getConnection()) {
            // SQL query to insert data into Employer table
            String sql = "INSERT INTO Employers (EmployerID, CompanyName, IndustryName, CompanyDescription, Location, "
                    + "Website, ContactPersonName, ContactPersonPhone, ContactPersonEmail, "
                    + "Username, Password) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            
            // Prepare the SQL statement
            try (PreparedStatement statement = connection.prepareStatement(sql)) {
                // Set parameters for the SQL statement
                statement.setString(1, EmployerID);
                statement.setString(2, companyName);
                statement.setString(3, industryName);
                statement.setString(4, companyDescription);
                statement.setString(5, location);
                statement.setString(6, website);
                statement.setString(7, contactPersonName);
                statement.setString(8, contactPersonPhone);
                statement.setString(9, contactPersonEmail);
                statement.setString(10, username);
                statement.setString(11, password);
                
                // Execute the SQL statement
                int rowsInserted = statement.executeUpdate();
                
                // Check if the employer was added successfully
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this, "A new Employer Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to add Employer", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException ex) {
            // Handle SQL exceptions
            ex.printStackTrace(); // Log the exception or display an error message
        }                                           

    }//GEN-LAST:event_btnAdminAddActionPerformed

