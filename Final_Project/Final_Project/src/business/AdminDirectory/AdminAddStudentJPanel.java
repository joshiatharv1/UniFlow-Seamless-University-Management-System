    private void btnAddStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStudentActionPerformed
        // TODO add your handling code here:
         String name = txtname.getText();
        String contact = txtcontact.getText();
        String dob = txtdob.getText();
        String gender = txtgender.getText();
        String nationality = txtnation.getText();
        String studentId = txtstuid.getText();
        String enrollmentStatus = txtenrollstat.getText();
        String emergencyContact = txtemergency2.getText();
        int creditsEarned = Integer.parseInt(txtcredit.getText());
        String username = txtUsername1.getText();
        String password = txtPassword.getText();
        String ProgramOfStudy= txtProgramofStudy.getText();
        
        try (Connection connection = DatabaseConnection.getConnection()) {
            // Create the SQL INSERT query
            String insertQuery = "INSERT INTO Students (Name, Contact, DOB, Gender, Nationality, StudentID, EnrollmentStatus, EmergencyContact, CreditsEarned, Username, Password, ProgramOfStudy) "
                                + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            
            // Prepare the statement
            try (PreparedStatement preparedStatement = connection.prepareStatement(insertQuery)) {
                // Set parameters for the INSERT query
                preparedStatement.setString(1, name);
                preparedStatement.setString(2, contact);
                preparedStatement.setString(3, dob);
                preparedStatement.setString(4, gender);
                preparedStatement.setString(5, nationality);
                preparedStatement.setString(6, studentId);
                preparedStatement.setString(7, enrollmentStatus);
                preparedStatement.setString(8, emergencyContact);
                preparedStatement.setInt(9, creditsEarned);
                preparedStatement.setString(10, username);
                preparedStatement.setString(11, password);
                preparedStatement.setString(12, ProgramOfStudy);

                // Execute the query
                preparedStatement.executeUpdate();
                
                // Display success message (you can customize this message)
                JOptionPane.showMessageDialog(this, "Student added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                // Clear the input fields after successful addition
            }
        } catch (SQLException e) {
            // Handle database errors
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error occurred while adding student. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddStudentActionPerformed


