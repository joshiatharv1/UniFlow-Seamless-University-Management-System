    private void deleteEmployee(String employeeID) {
        try (Connection connection = DatabaseConnection.getConnection()) {
            String query = "DELETE FROM faculty WHERE EmployeeID = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, employeeID);

            int rowsAffected = preparedStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Employee deleted successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Employee not found!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Refresh the table after deletion
            displayFacultyDetails();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void displayFacultyDetails() {
        DefaultTableModel model = (DefaultTableModel) tblEmployeeDetails.getModel();
        model.setRowCount(0);

        try (Connection connection = DatabaseConnection.getConnection()) {
            String query = "SELECT EmployeeID, Name, Position, Department FROM faculty";
            PreparedStatement preparedStatement = connection.prepareStatement(query);

            ResultSet resultSet = preparedStatement.executeQuery();
            while (resultSet.next()) {
                String employeeID = resultSet.getString("EmployeeID");
                String name = resultSet.getString("Name");
                String position = resultSet.getString("Position");
                String department = resultSet.getString("Department");
                model.addRow(new Object[]{employeeID, name, position, department});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
