    private void btnAddFacultyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFacultyActionPerformed
        // TODO add your handling code here:

    try (Connection connection = DatabaseConnection.getConnection()) {
        String sql = "INSERT INTO faculty (Name, Contact, DOB, Gender, Nationality, Department, LanguageTaught, " +
                     "Position, EmergencyContact, Email, Username, Password) " +
                     "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

        try (PreparedStatement preparedStatement = connection.prepareStatement(sql)) {
            preparedStatement.setString(1, txtName.getText());
            preparedStatement.setString(2, txtContact.getText());
            preparedStatement.setString(3, txtDOB.getText());
            preparedStatement.setString(4, txtGender.getText());
            preparedStatement.setString(5, txtNationality.getText());
            preparedStatement.setString(6, txtDepartment.getText());
            preparedStatement.setString(7, txtLanguageTaught.getText());
            preparedStatement.setString(8, txtPosition.getText());
            preparedStatement.setString(9, txtEmergencyContact.getText());
            preparedStatement.setString(10, txtEmail.getText());
            preparedStatement.setString(11, txtUsername.getText());
            preparedStatement.setString(12, txtPassword.getText());

            int rowsAffected = preparedStatement.executeUpdate();

            if (rowsAffected > 0) {
                System.out.println("Faculty added successfully!");
                JOptionPane.showMessageDialog(this, "Faculty added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                System.out.println("Failed to add faculty.");
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnAddFacultyActionPerformed

