    private void displayEmployerDetails() {
        DefaultTableModel model = (DefaultTableModel) tblManageEmployer.getModel();
        model.setRowCount(0);

        try (Connection connection = DatabaseConnection.getConnection()) {
            String query = "SELECT EmployerID, ContactPersonName, ContactPersonPhone, ContactPersonEmail FROM employers";
            PreparedStatement preparedStatement = connection.prepareStatement(query);

            ResultSet resultSet = preparedStatement.executeQuery();
            while (resultSet.next()) {
                String employerID = resultSet.getString("EmployerID");
                String contactPersonName = resultSet.getString("ContactPersonName");
                String contactPersonPhone = resultSet.getString("ContactPersonPhone");
                String contactPersonEmail = resultSet.getString("ContactPersonEmail");
                model.addRow(new Object[]{employerID, contactPersonName, contactPersonPhone, contactPersonEmail});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    private void deleteEmployer(String employerID) {
        try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/university", "root", "Mysql@1")) {
            String query = "DELETE FROM employers WHERE EmployerID = ?";
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setString(1, employerID);

            int rowsAffected = preparedStatement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Employer deleted successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Employer not found!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Refresh the table after deletion
            displayEmployerDetails();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
