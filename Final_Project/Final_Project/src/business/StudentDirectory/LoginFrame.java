    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try {
        Connection connection = DatabaseConnection.getConnection();
        String username = txtusername.getText();
        char[] passwordChars = txtpassword.getPassword();
        String enteredPassword = new String(passwordChars);

        String query = "SELECT * FROM Students WHERE Username=?";
        PreparedStatement preparedStatement = connection.prepareStatement(query);
        preparedStatement.setString(1, username);

        ResultSet resultSet = preparedStatement.executeQuery();

        if (resultSet.next()) {
            // Retrieve the stored password from the database
            String storedPassword = resultSet.getString("Password");

            // Check if the stored password is hashed
            boolean isHashed = storedPassword.startsWith("$2a$");

            // Verify the password
            if (isHashed && BCrypt.checkpw(enteredPassword, storedPassword)) {
                // Password is hashed and matches
                JOptionPane.showMessageDialog(null, "Welcome Student!");
                StudentJPanel studentPanel = new StudentJPanel(username);
                studentPanel.setUsername(username);

                // Set up the StudentJPanel in a JFrame
                JFrame studentFrame = new JFrame("Student Panel");
                studentFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                studentFrame.setSize(new Dimension(900, 800));
                studentFrame.add(studentPanel);
                studentFrame.setLocationRelativeTo(null);
                studentFrame.setVisible(true);

                // Close the current login frame
                dispose();
            } else if (!isHashed && enteredPassword.equals(storedPassword)) {
                // Password is not hashed and matches
                JOptionPane.showMessageDialog(null, "Welcome Student!");
                StudentJPanel studentPanel = new StudentJPanel(username);
                studentPanel.setUsername(username);

                // Set up the StudentJPanel in a JFrame
                JFrame studentFrame = new JFrame("Student Panel");
                studentFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                studentFrame.setSize(new Dimension(900, 800));
                studentFrame.add(studentPanel);
                studentFrame.setLocationRelativeTo(null);
                studentFrame.setVisible(true);

                // Close the current login frame
                dispose();
            } else {
                // Password does not match
                JOptionPane.showMessageDialog(null, "Invalid login credentials!");
            }
        } else {
            // Username not found
            JOptionPane.showMessageDialog(null, "Invalid username!");
        }

        resultSet.close();
        preparedStatement.close();
        connection.close();
    }  catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
            //                } catch (ClassNotFoundException ex) {
            //            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

