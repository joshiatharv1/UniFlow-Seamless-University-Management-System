    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String languageToSearch = txtsearchprofessor.getText().trim();
    
    // Database connection parameters
    String query = "SELECT Name, CourseTaught, Department, Position FROM faculty WHERE LanguageTaught LIKE ?";

    try (Connection connection = DatabaseConnection.getConnection();
         PreparedStatement preparedStatement = connection.prepareStatement(query)) {

        // Set the parameter in the SQL query
        preparedStatement.setString(1, "%" + languageToSearch + "%");

        try (ResultSet resultSet = preparedStatement.executeQuery()) {
            DefaultTableModel model = (DefaultTableModel) tblprofessorsearch.getModel();
            model.setRowCount(0); // Clear existing table data
            
            // Populate the table model with data from the ResultSet
            while (resultSet.next()) {
                String name = resultSet.getString("Name");
                String courseTaught = resultSet.getString("CourseTaught");
                String department = resultSet.getString("Department");
                String position = resultSet.getString("Position");

                // Add a row to the table model
                model.addRow(new Object[]{name, courseTaught, department, position});
            }
        }
    } catch (SQLException ex) {
        // Handle exceptions (e.g., show an error message).
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace(); // Logging the exception might be helpful for debugging
    }

        
    }//GEN-LAST:event_btnSearchActionPerformed


